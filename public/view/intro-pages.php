
Série 1

Voici une liste d’activités. 
Cochez les activités que vous aimez ou que vous aimeriez pratiquer. 
Il est important de ne pas faire de croix si l’activité vous déplaît ou vous laisse indifférent(e). Pour remplir cette partie, considérez que vous avez les capacités, les connaissances et la formation nécessaires pour effectuer ces activités. Tenez compte uniquement de vos goûts.


Série 2

Voici une liste de métiers.
Cochez les professions qui vous conviendraient. 
Ne vous préoccupez pas de savoir si vous pouvez réussir dans tel métier ou dans telle profession, ni de votre préparation actuelle. Ne songez ni au salaire, ni à l’avancement mais demandez-vous si le travail en question est susceptible de vous plaire.


Série 3

Voici une liste d’aptitudes, d’habiletés, de talents qui sont en rapport avec des occupations.
Évaluez-vous le plus objectivement possible en choisissant le signe/ le chiffre qui correspond au niveau d’aptitudes que vous pensez posséder : 
    • Faible =  -  1
    • Moyen =  + 2,
    •  Fort = ++ 3  (ou les signes?)
Ne vous sous-estimez pas ou ne vous surestimez pas. Soyez honnête envers vous-mêmes et évaluez votre capacité réelle.

Série 4

Voici une liste de traits de caractère, d’attitudes, de tendances personnelles. 
Cochez les énoncés qui s’appliquent à vous, et décrivent le plus honnêtement possible votre façon d’agir.
